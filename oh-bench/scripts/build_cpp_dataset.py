def get_cpp_category(rule_id):
    clean = rule_id.replace("cppcheck/", "")
    for cat, keywords in CPP_CATEGORIES.items():
        for kw in keywords:
            if kw.lower() in clean.lower():
                return cat
    return "Other"


def find_function_boundaries(lines, target_idx):
    n = len(lines)
    start = 0
    end = n

    for i in range(target_idx, -1, -1):
        line = lines[i]
        if re.match(r'^}', line):
            start = i + 1
            break
        if re.search(r'\w+\s*\(.*\)\s*(\w+)*\s*\{', line) and not line.strip().startswith('//'):
            if i > 0 and 'template' in lines[i-1]:
                start = i - 1
            else:
                start = i
            if len(line) - len(line.lstrip()) < 4:
                break

    for i in range(target_idx, n):
        line = lines[i]
        if re.match(r'^}', line):
            end = i + 1
            break

    if end - start > 300 or end - start < 5:
        start = max(0, target_idx - 50)
        end = min(n, target_idx + 50)

    return start, end


def is_auto_generated(content):
    markers = [
        "AUTO GENERATED", "auto-generated", "DO NOT EDIT",
        "Generated by", "generated by", "GENERATED CODE",
        "This file is generated", "DO NOT MODIFY"
    ]
    # 只检查前30行
    head = '\n'.join(content.splitlines()[:30])
    return any(m in head for m in markers)


def is_mostly_empty(code):
    lines = code.splitlines()
    real_code = [l for l in lines
                 if l.strip()
                 and not l.strip().startswith('//')
                 and not l.strip().startswith('*')
                 and not l.strip().startswith('/*')]
    return len(real_code) < 3


class CppProcessor:
    def __init__(self, root):
        self.root = root
        self.data = []
        self.seen_hash = defaultdict(set)
        self.seen_location = set()
        self.counts = defaultdict(int)
        self.total = 0
        self.stats = defaultdict(int)

    def process(self, rec):
        self.stats["total"] += 1

        rule_id = rec['Rule_ID']
        if rule_id not in TARGET_CPP_RULES:
            return False

        limit = TARGET_CPP_RULES.get(rule_id, 0)
        if self.counts[rule_id] >= limit:
            self.stats["skip_full"] += 1
            return False

        repo_name = rec['Repository']
        rel_path = rec['File_Path']
        line = int(rec['Line_No'])

        if rule_id == "cppcheck/functionStatic" and rel_path.endswith('.h'):
            self.stats["skip_filter"] += 1
            return False

        loc_key = (repo_name, rel_path, line, rule_id)
        if loc_key in self.seen_location:
            self.stats["skip_dup_loc"] += 1
            return False

        full_path = os.path.join(self.root, repo_name, rel_path)
        if not os.path.exists(full_path):
            self.stats["skip_no_file"] += 1
            return False

        try:
            with open(full_path, 'r', encoding='utf-8', errors='ignore') as f:
                content = f.read()
        except:
            self.stats["skip_read_error"] += 1
            return False

        if is_auto_generated(content):
            self.stats["skip_auto_gen"] += 1
            return False

        lines = content.splitlines(keepends=True)
        idx = max(0, line - 1)

        start, end = find_function_boundaries(lines, idx)
        code = "".join(lines[start:end])

        if not code or len(code.strip()) < 10:
            self.stats["skip_too_short"] += 1
            return False

        code_lines = len(code.splitlines())
        # if code_lines > 2000:
        #     self.stats["skip_too_long"] += 1
        #     return False

        if is_mostly_empty(code):
            self.stats["skip_empty"] += 1
            return False

        h = hashlib.md5(code.encode()).hexdigest()
        if h in self.seen_hash[rule_id]:
            self.stats["skip_dup_hash"] += 1
            return False

        buggy_files = {rel_path: content}

        self.seen_location.add(loc_key)
        self.seen_hash[rule_id].add(h)
        self.counts[rule_id] += 1
        self.total += 1
        self.stats["ok"] += 1

        self.data.append({
            "instance_id": f"CPP_{self.total:04d}",
            "project": repo_name,
            "rule_id": rule_id,
            "commit_hash": "",

            "buggy_files": buggy_files,
            "target_file": rel_path,

            "input_snippet": code,
            "start_line": start + 1,
            "end_line": end,

            "category": get_cpp_category(rule_id),
            "line_number": line,
            "warning_message": rec['Message'],
            "severity": rec.get('Severity', 'warning'),
            "code_lines": code_lines,
        })
        return True


def main():
    if not os.path.exists(CSV_FILE):
        print(f"错误: 找不到 {CSV_FILE}")
        return

    print("=" * 60)
    print("C++ 数据集构建")
    print("=" * 60)

    proc = CppProcessor(PROJECT_ROOT)

    avail = defaultdict(int)
    rows = []
    with open(CSV_FILE, 'r', encoding='utf-8-sig') as f:
        reader = csv.DictReader(f)
        for row in reader:
            rows.append(row)
            rid = row.get('Rule_ID', '')
            if rid in TARGET_CPP_RULES:
                avail[rid] += 1

    print(f"\n总行数: {len(rows)}")
    print(f"\n规则可用情况:")
    for rule in sorted(TARGET_CPP_RULES.keys()):
        a = avail.get(rule, 0)
        t = TARGET_CPP_RULES[rule]
        short = rule.replace("cppcheck/", "")
        cat = get_cpp_category(rule)
        print(f"  [{cat}] {short:<30} 可用:{a:>5}  目标:{t}")

    print(f"\n处理中...")
    for i, row in enumerate(rows):
        proc.process(row)
        if (i + 1) % 1000 == 0:
            print(f"\r  处理: {i + 1}/{len(rows)}  采集: {proc.total}", end="", flush=True)

    print(f"\n\n采集完成: {proc.total} 条")

    with open(OUTPUT_JSON, 'w', encoding='utf-8') as f:
        json.dump(proc.data, f, indent=2, ensure_ascii=False)

    print(f"输出: {OUTPUT_JSON}")

    print(f"\n{'='*60}")
    print("统计")
    print(f"{'='*60}")

    cats = defaultdict(int)
    for d in proc.data:
        cats[d["category"]] += 1
    print(f"  分类: A={cats.get('A',0)} B={cats.get('B',0)} C={cats.get('C',0)} D={cats.get('D',0)}")

    s = proc.stats
    print(f"\n  采集={s['ok']}")
    print(f"  跳过:")
    print(f"    满额={s.get('skip_full',0)}")
    print(f"    文件缺失={s.get('skip_no_file',0)}")
    print(f"    位置重复={s.get('skip_dup_loc',0)}")
    print(f"    内容重复={s.get('skip_dup_hash',0)}")
    print(f"    过短={s.get('skip_too_short',0)}")
    print(f"    过长={s.get('skip_too_long',0)}")
    print(f"    自动生成={s.get('skip_auto_gen',0)}")
    print(f"    纯注释={s.get('skip_empty',0)}")
    print(f"    过滤={s.get('skip_filter',0)}")

    print(f"\n  规则结果:")
    for rule in sorted(TARGET_CPP_RULES.keys()):
        act = proc.counts[rule]
        tgt = TARGET_CPP_RULES[rule]
        short = rule.replace("cppcheck/", "")
        cat = get_cpp_category(rule)
        print(f"    [{cat}] {short:<28} {act:>3}/{tgt}")

    lens = [d['code_lines'] for d in proc.data]
    if lens:
        print(f"\n  代码长度: 平均{sum(lens)/len(lens):.0f}行, 范围{min(lens)}-{max(lens)}行")


if __name__ == "__main__":
    main()